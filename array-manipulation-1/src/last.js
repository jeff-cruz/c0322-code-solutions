/* exported last */
function last(array) {
  var lastIndex = array.length - 1;
  var value = array[lastIndex];
  return value;
}
// 1. access last index of array by getting length of array and subtract one
// 2. assign result of expression to new variable lastIndex
// 3. access value of array at lastIndex
// 4. assign result of expression to new variable value
// 5. return value
